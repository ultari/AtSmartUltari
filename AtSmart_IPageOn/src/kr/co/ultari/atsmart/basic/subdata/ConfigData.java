package kr.co.ultari.atsmart.basic.subdata;

public class ConfigData {
        public String key;
        public String title;
        public String value;
        public String info;

        public ConfigData( String key, String title, String value, String info )
        {
                this.key = key;
                this.title = title;
                this.value = value;
                this.info = info;
        }
}
